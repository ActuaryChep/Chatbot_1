    "# --------------------- RESPONSE FUNCTIONS ---------------------\n",
    "\n",
    "def get_faq_response(query, threshold=0.6):\n",
    "    query_vec = vectorizer.transform([query])\n",
    "    similarity = cosine_similarity(query_vec, tfidf_matrix).flatten()\n",
    "    max_sim = similarity.max()\n",
